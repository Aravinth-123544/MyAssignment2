package week1day2;

public class NegativeToPositiveConverter {
	 public static void main(String[] args) {
	        int number = -40;

	        if (number < 0) {
	            int positiveNumber = -number;
	            System.out.println("The number " + number + " is converted to " + positiveNumber);
	        } else {
	            System.out.println("The number is not negative");
	        }
	    }
	}
	


















